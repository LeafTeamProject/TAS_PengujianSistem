    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        UserController nc = new UserController();

        // Mengambil nilai dari input pengguna
        String nama = this.txtNama.getText().trim();  // Menghapus spasi ekstra di sekitar ID

        // Mengambil tanggal dari JDateChooser dan memformatnya ke string yyyy-MM-dd
        java.util.Date selectedDate = this.date.getDate();
        if (selectedDate == null) {
            JOptionPane.showMessageDialog(this, "Tanggal tidak boleh kosong!");
            return;  // Keluar jika tanggal tidak dipilih
        }
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String formattedDate = dateFormat.format(selectedDate);  // Mengonversi tanggal ke string dengan format yang benar

        String statusAbsen = this.status.getSelectedItem().toString();
        // Memanggil metode tambahUser dengan parameter ID, nama, hariAbsen (tanggal), dan password
        if(!nama.equals(this.a)){
            JOptionPane.showMessageDialog(this, "Nama Tidak Sesuai", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
            boolean status = nc.tambahUser(nama, statusAbsen , formattedDate);
            if (status) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan");
                DefaultTableModel dtm = nc.createTable();
                this.tbkaryawan.setModel(dtm);
                nc.tampilkanUser(this.a);  // Menampilkan data terbaru
            } else {
                JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan", "Error", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

