    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Create a JFileChooser instance
        JFileChooser fileChooser = new JFileChooser();

        // Set the dialog title
        fileChooser.setDialogTitle("Pilih File");

        // Set file selection mode (files only)
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);

        // Show the open dialog and capture the result
        int result = fileChooser.showOpenDialog(this);

        // Handle the result
        if (result == JFileChooser.APPROVE_OPTION) {
            // Get the selected file
            File selectedFile = fileChooser.getSelectedFile();

            try {
                InputStream barInputStream = new FileInputStream(selectedFile.getAbsolutePath());
                BufferedImage barBufferedImage = ImageIO.read(barInputStream);
                LuminanceSource source = new BufferedImageLuminanceSource(barBufferedImage);
                BinaryBitmap bitmap = new BinaryBitmap(new HybridBinarizer(source));
                MultiFormatReader reader = new MultiFormatReader();
                Result hasil = reader.decode(bitmap);
                txtNama.setText(hasil.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error", "Error", JOptionPane.WARNING_MESSAGE);
            }
            JOptionPane.showMessageDialog(this, "File Terpilih : " + selectedFile.getAbsolutePath());
        } else if (result == JFileChooser.CANCEL_OPTION) {
            // User canceled the operation
            JOptionPane.showMessageDialog(this, "Pemilihan File dibatalkan");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

